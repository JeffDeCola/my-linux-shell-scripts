#!/bin/bash -e
# clean-docker.sh

echo " "
echo "************************************************************************"
echo "********************************************** clean-docker.sh (START) *"
echo " "

echo "(docker ps -a -q)"
echo "------------------------------------------------------------------------"
echo " "
docker rm $(docker ps -a -q)

echo "(docker images -q)"
echo "------------------------------------------------------------------------"
echo " "
docker rmi $(docker images -q)

echo "************************************************** clean-docker.sh (END) *"
echo "**************************************************************************"
echo " "
